# Log Session integer, float, string, and boolean variables, and use the type() function to print each data type.

# Integer
num = 10

# Float
price = 99.99

# String
name = "Vamshi"

# Boolean
is_active = True

# Printing values and their data types
print("Value:", num, "Type:", type(num))
print("Value:", price, "Type:", type(price))
print("Value:", name, "Type:", type(name))
print("Value:", is_active, "Type:", type(is_active))