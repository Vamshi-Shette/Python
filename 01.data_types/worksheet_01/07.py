# Convert a numeric string ("50") to an integer and a float, then print the converted values and their types.

# Numeric string
num_str = "50"

# Convert to integer
num_int = int(num_str)

# Convert to float
num_float = float(num_str)

# Print results
print("Integer Value:", num_int, "Type:", type(num_int))
print("Float Value:", num_float, "Type:", type(num_float))