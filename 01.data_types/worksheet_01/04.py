#Compare two integers and print boolean results for each comparison (==, !=, >, <, >=, <=).
var1=int(input("input 1: "))
var2=int(input("input 2: "))
print(f'var1 == var2: {var1==var2}')
print(f'var1 != var2: {var1!=var2}')
print(f'var1 > var2: {var1>var2}')
print(f'var1 < var2: {var1<var2}')
print(f'var1 >= var2: {var1>=var2}')
print(f'var1 <= var2: {var1<=var2}')