# Perform arithmetic between integer (10) and float (3.5) variables, then print the result and its type.

# Assign values
a = 10        # integer
b = 3.5       # float

# Perform arithmetic operations
addition = a + b
multiplication = a * b

# Print results
print("Addition Result:", addition, "| Type:", type(addition))
print("Multiplication Result:", multiplication, "| Type:", type(multiplication))