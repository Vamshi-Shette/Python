# Swap two variable values in one line and print the values before and after swapping.

a = 10
b = 20

# Print before swapping
print("Before Swapping:")
print("a =", a)
print("b =", b)

# Swap in one line
a, b = b, a

# Print after swapping
print("\nAfter Swapping:")
print("a =", a)
print("b =", b)