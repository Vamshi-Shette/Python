# Assign an integer to a variable, then reassign it to a float and string sequentially, printing type and value after each step.
# Assign integer
value = 100
print("Value:", value, "| Type:", type(value))

# Reassign to float
value = 99.99
print("Value:", value, "| Type:", type(value))

# Reassign to string
value = "Python"
print("Value:", value, "| Type:", type(value))